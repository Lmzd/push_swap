#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*tmp1;
	unsigned char	*tmp2;

	tmp1 = (unsigned char *)s1;
	tmp2 = (unsigned char *)s2;
	if (n)
	{
		i = 0;
		while (*tmp1 && *tmp1 == *tmp2 && i < n - 1)
		{
			++tmp1;
			++tmp2;
			i++;
		}
		return (*tmp1 - *tmp2);
	}
	return (0);
}
