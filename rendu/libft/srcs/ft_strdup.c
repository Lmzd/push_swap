#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	s_len;
	char	*dest;
	int		i;

	s_len = ft_strlen(s);
	if (!(s))
		return (NULL);
	if (!(dest = (char *)malloc(sizeof(*dest) * s_len + 1)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
