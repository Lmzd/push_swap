#include "libft.h"

void	*ft_memchr(const void *src, int c, size_t n)
{
	const unsigned char *s;
	size_t				i;

	i = 0;
	s = (unsigned char*)src;
	while (i < n)
	{
		if (s[i] == (unsigned char)c)
			return ((unsigned char*)(s + i));
		i++;
	}
	return (NULL);
}
